package r.k.b.u;

public class h
{
    private final double syc;
    private final double sya;
    private final boolean sym;
    public static final int syh;
    public static final boolean sxd;
    
    public h(final double syc, final double sya, final boolean sym) {
        super();
        this.syc = syc;
        this.sya = sya;
        this.sym = sym;
    }
    
    public double yu() {
        return this.syc;
    }
    
    public double yz() {
        return this.sya;
    }
    
    public boolean u() {
        return this.sym;
    }
}
